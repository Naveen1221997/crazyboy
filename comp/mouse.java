package comp;

public class mouse
{
	private String name;
	private int keys;
	private keyboard keyboard;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getKeys() {
		return keys;
	}
	public void setKeys(int keys) {
		this.keys = keys;
	}
	public keyboard getKeyboard() {
		return keyboard;
	}
	public void setKeyboard(keyboard keyboard) {
		this.keyboard = keyboard;
	}
	

}
